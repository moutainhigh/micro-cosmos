package com.yichen.cosmos.cloud.platform.bean.rule_bean.basicinfo;

public class LivingInfo {

    private String livingProperty;
    private String livingPropertyResult;
    private String livingLenResult;
    private Double livingPropertyScore;
    private Double livingLenScore;
    private Integer livingLen;


    public String getLivingProperty() {
        return livingProperty;
    }

    public void setLivingProperty(String livingProperty) {
        this.livingProperty = livingProperty;
    }

    public String getLivingPropertyResult() {
        return livingPropertyResult;
    }

    public void setLivingPropertyResult(String livingPropertyResult) {
        this.livingPropertyResult = livingPropertyResult;
    }

    public String getLivingLenResult() {
        return livingLenResult;
    }

    public void setLivingLenResult(String livingLenResult) {
        this.livingLenResult = livingLenResult;
    }

    public Double getLivingPropertyScore() {
        return livingPropertyScore;
    }

    public void setLivingPropertyScore(Double livingPropertyScore) {
        this.livingPropertyScore = livingPropertyScore;
    }

    public Double getLivingLenScore() {
        return livingLenScore;
    }

    public void setLivingLenScore(Double livingLenScore) {
        this.livingLenScore = livingLenScore;
    }

    public Integer getLivingLen() {
        return livingLen;
    }

    public void setLivingLen(Integer livingLen) {
        this.livingLen = livingLen;
    }

}

