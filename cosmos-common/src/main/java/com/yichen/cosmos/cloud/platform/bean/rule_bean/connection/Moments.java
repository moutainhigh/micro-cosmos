public class Moments {

    private String moments;
    private String phonenumResult;
    private String momentsResult;
    private Double phonenumScore;
    private Double momentsScore;
    private Integer phonenum;


    public String getMoments() {
        return moments;
    }

    public void setMoments(String moments) {
        this.moments = moments;
    }

    public String getPhonenumResult() {
        return phonenumResult;
    }

    public void setPhonenumResult(String phonenumResult) {
        this.phonenumResult = phonenumResult;
    }

    public String getMomentsResult() {
        return momentsResult;
    }

    public void setMomentsResult(String momentsResult) {
        this.momentsResult = momentsResult;
    }

    public Double getPhonenumScore() {
        return phonenumScore;
    }

    public void setPhonenumScore(Double phonenumScore) {
        this.phonenumScore = phonenumScore;
    }

    public Double getMomentsScore() {
        return momentsScore;
    }

    public void setMomentsScore(Double momentsScore) {
        this.momentsScore = momentsScore;
    }

    public Integer getPhonenum() {
        return phonenum;
    }

    public void setPhonenum(Integer phonenum) {
        this.phonenum = phonenum;
    }

}

