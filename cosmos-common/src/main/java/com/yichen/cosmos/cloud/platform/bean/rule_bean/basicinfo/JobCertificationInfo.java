package com.yichen.cosmos.cloud.platform.bean.rule_bean.basicinfo;

public class JobCertificationInfo {

    private String certificateLevel;
    private String certificateLenResult;
    private String certificateLevelResult;
    private Double certificateLenScore;
    private Double certificateLevelScore;
    private Integer certificateLen;


    public String getCertificateLevel() {
        return certificateLevel;
    }

    public void setCertificateLevel(String certificateLevel) {
        this.certificateLevel = certificateLevel;
    }

    public String getCertificateLenResult() {
        return certificateLenResult;
    }

    public void setCertificateLenResult(String certificateLenResult) {
        this.certificateLenResult = certificateLenResult;
    }

    public String getCertificateLevelResult() {
        return certificateLevelResult;
    }

    public void setCertificateLevelResult(String certificateLevelResult) {
        this.certificateLevelResult = certificateLevelResult;
    }

    public Double getCertificateLenScore() {
        return certificateLenScore;
    }

    public void setCertificateLenScore(Double certificateLenScore) {
        this.certificateLenScore = certificateLenScore;
    }

    public Double getCertificateLevelScore() {
        return certificateLevelScore;
    }

    public void setCertificateLevelScore(Double certificateLevelScore) {
        this.certificateLevelScore = certificateLevelScore;
    }

    public Integer getCertificateLen() {
        return certificateLen;
    }

    public void setCertificateLen(Integer certificateLen) {
        this.certificateLen = certificateLen;
    }

}

