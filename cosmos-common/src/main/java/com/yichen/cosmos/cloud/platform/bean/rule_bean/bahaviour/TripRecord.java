public class TripRecord {

    private String tripType;
    private String tripDest;
    private String tripnumResult;
    private String tripTypeResult;
    private String tripDestResult;
    private Double tripnumScore;
    private Double tripTypeScore;
    private Double tripDestScore;
    private Integer tripnum;


    public String getTripType() {
        return tripType;
    }

    public void setTripType(String tripType) {
        this.tripType = tripType;
    }

    public String getTripDest() {
        return tripDest;
    }

    public void setTripDest(String tripDest) {
        this.tripDest = tripDest;
    }

    public String getTripnumResult() {
        return tripnumResult;
    }

    public void setTripnumResult(String tripnumResult) {
        this.tripnumResult = tripnumResult;
    }

    public String getTripTypeResult() {
        return tripTypeResult;
    }

    public void setTripTypeResult(String tripTypeResult) {
        this.tripTypeResult = tripTypeResult;
    }

    public String getTripDestResult() {
        return tripDestResult;
    }

    public void setTripDestResult(String tripDestResult) {
        this.tripDestResult = tripDestResult;
    }

    public Double getTripnumScore() {
        return tripnumScore;
    }

    public void setTripnumScore(Double tripnumScore) {
        this.tripnumScore = tripnumScore;
    }

    public Double getTripTypeScore() {
        return tripTypeScore;
    }

    public void setTripTypeScore(Double tripTypeScore) {
        this.tripTypeScore = tripTypeScore;
    }

    public Double getTripDestScore() {
        return tripDestScore;
    }

    public void setTripDestScore(Double tripDestScore) {
        this.tripDestScore = tripDestScore;
    }

    public Integer getTripnum() {
        return tripnum;
    }

    public void setTripnum(Integer tripnum) {
        this.tripnum = tripnum;
    }

}

